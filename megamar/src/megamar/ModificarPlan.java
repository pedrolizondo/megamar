private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    String semanas = txtsemanas.getText();
    String interes = txtinteres.getText();
    String comision = txtcomision.getText();
     if(Integer.parseInt(comision) > 5){
        JOptionPane.showMessageDialog(null, "Error! La comision no puede ser mayor a 5%. ","Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    String consulta = "update plan "
            + "set semanas='" + semanas + "', interes='" + interes + "', comision='" + comision + "' "
            + "where idplan='" + idplan + "'";
    
    try{
          Conectar();
            int done= stmt.executeUpdate(consulta);
            //JOptionPane.showMessageDialog(null,"alta","Aviso",JOptionPane.INFORMATION_MESSAGE);
            JOptionPane.showMessageDialog(null,"Se actualizaron los datos del plan.","Aviso", JOptionPane.INFORMATION_MESSAGE);
            db.close(stmt);
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex.getMessage(),"Error! No se pudo actualizar los datos del cliente.",JOptionPane.ERROR_MESSAGE);
        }
    db.destroy();
    this.dispose(); 
}//GEN-LAST:event_jButton2ActionPerformed

