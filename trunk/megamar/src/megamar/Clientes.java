private void jbguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbguardarActionPerformed
    String nombre = txtnombre.getText();
    String apellido = txtapellido.getText();
    String domicilio_particular = txtdomicilioparticular.getText();
    String barrio_particular = txtbarrioparticular.getText();
    String domicilio_comercial = txtdomiciliocomercial.getText();
    String barrio_comercial = txtbarriocomercial.getText();
    String telefono = txttelefono.getText();
    int num_creditos = 0;   //Cuando se da de alta un cliente se guarda como /0
    String perno = "NO";    //Perno = 0
    String estado = "NUEVO";
    int rubro = comborubro.getSelectedIndex();
    //String idz = idzona;
    
    if(txtdni.getText().equals("")){
        JOptionPane.showMessageDialog(null,"Ingrese el DNI del Cliente.","Error!",JOptionPane.ERROR_MESSAGE);
        return;
    }
    if(existedni(txtdni.getText())){
        txtdni.requestFocus();
        return;
    }
    if(nombre.equals("")){
        JOptionPane.showMessageDialog(null,"Ingrese el Nombre del Cliente.","Error!",JOptionPane.ERROR_MESSAGE);
        return;
    }
    if(apellido.equals("")){
        JOptionPane.showMessageDialog(null,"Ingrese el Apellido del Cliente.","Error!",JOptionPane.ERROR_MESSAGE);
        return;
    }
    if(rubro == 0){
        JOptionPane.showMessageDialog(null,"Seleccione un Rubro de la lista.","Error!",JOptionPane.ERROR_MESSAGE);
        return;
    }
    if (altacliente == false) {
        JOptionPane.showMessageDialog(null, "Error! No se pude dar de alta al cliente porque ya se encuentra registrado en la Base de Datos.", "Error!", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    //int idcliente = Integer.parseInt(txtidcliente.getText());
    int dni = Integer.parseInt(txtdni.getText());
    
    String consulta = "insert into cliente(dni, nombre, apellido, domicilio_particular, barrio_particular, domicilio_comercial, barrio_comercial, telefono, num_creditos, perno, estado, idzona, idrubro) "
            + "values('"+dni+"', '"+nombre+"', '"+apellido+"', '"+domicilio_particular+"', '"+barrio_particular+"', '"+domicilio_comercial+"', '"+barrio_comercial+"', "
            + " '"+telefono+"', '"+num_creditos+"', '"+perno+"', '"+estado+"', '"+idzona+"', '"+rubro+"' )";
    try{
          Conectar();
            int done= stmt.executeUpdate(consulta);
            //JOptionPane.showMessageDialog(null,"alta","Aviso",JOptionPane.INFORMATION_MESSAGE);
            JOptionPane.showMessageDialog(null,"Se guardaron los datos del nuevo cliente","Aviso", JOptionPane.INFORMATION_MESSAGE);
            db.close(stmt);
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex.getMessage(),"Error! No se pudo cargar los datos del cliente",JOptionPane.ERROR_MESSAGE);
        }
      db.destroy();
      this.dispose();
}//GEN-LAST:event_jbguardarActionPerformed

